package com.premsh.visitorsure.models;

public class Employee {
	
	private Integer employeeId;
	private Integer officeId;
	private String employeeIdInternal;
	private String firstName;
	private String lastName;
	private String email;
	private String phone;
	private String profiePhotoUrl;
	
	public Integer getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(Integer employeeId) {
		this.employeeId = employeeId;
	}
	public Integer getOfficeId() {
		return officeId;
	}
	public void setOfficeId(Integer officeId) {
		this.officeId = officeId;
	}
	public String getEmployeeIdInternal() {
		return employeeIdInternal;
	}
	public void setEmployeeIdInternal(String employeeIdInternal) {
		this.employeeIdInternal = employeeIdInternal;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getProfiePhotoUrl() {
		return profiePhotoUrl;
	}
	public void setProfiePhotoUrl(String profiePhotoUrl) {
		this.profiePhotoUrl = profiePhotoUrl;
	}
	

}
